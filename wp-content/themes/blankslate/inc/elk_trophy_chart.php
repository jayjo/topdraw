		<p class="centery">Colorado is a top destination for archery elk hunters.   Managed primarily on hunting opportunity, it ranks #1 in total P&Y elk entries averaging almost 60 a year.  The downside to such great hunting opportunity is obvious when looking at the average size of Colorado’s P&Y entries.  At 306 1/8”, only New Mexico’s average bull is smaller.  Top end bulls are few and far between as well.  Considering that the best P&Y elk to come from Colorado over that last few years doesn’t break the 380 mark, along with the fact that the top hunts require over 20 points to draw and it’s pretty tough to recommend waiting out a ‘top end’ unit for that kind of upside.  Rather, it may be worth considering a middle of the road area in hopes of a shot at a 350 class bull.  There are plenty of counties turning out that type of bull each year, often in units that take less than 5 points to draw.</p>
