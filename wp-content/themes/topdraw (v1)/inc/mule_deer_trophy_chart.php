	<div class="table_toggle">
		<p class="centery">Colorado mule deer hunting is as good as it gets in the west!  Offering a rare combination of tops in number of entries as well as the biggest bucks taken in any state in recent years, everyone should be building points for deer in Colorado.  Of the Rocky Mountain States, over a third of all P&Y mule deer entries come out of Colorado.  Despite a dip in the number entries over the last few seasons, it still leads the way with around 27 a year.  About 4 of them hit the 180” mark and usually one of these is a non-typical giant scoring in the mid 230’s”. Best of all, there isn’t an archery hunt in the state that requires more than 9 points to draw and most require 3 or less points to draw.  Throw in an average harvest rate of around 30% on draw hunts and there’s a lot to be excited about hunting mule deer in Colorado!  If you want to hunt mule deer in the Rockies, along with spending time in some of the most pristine landscapes the country has to offer, put Colorado at the top of your list!</p>
