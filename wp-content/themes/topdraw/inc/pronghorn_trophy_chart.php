		<p class="centery">Colorado pronghorn hunting probably doesn’t get quite as much attention as it deserves.  Viewed by many as the worst state in the Rockies to chase speedgoats, only Wyoming and Montana turn out more P&Y entries each year.  These numbers have been dropping over the last few years but that has been a common trend in most states.   Top end bucks can be hard to come by, but even so, only Arizona and Utah turn out a higher ratio of 80” bucks per entry.  Most of the units with the highest archery success rates take the most points to draw although there are a few hunts that are averaging 50% success rates and can be had with 5 or less points.  As is the case with most pronghorn hunting, private land can be an issue.  But, with a little homework and a land ownership chip in your GPS, hunting Colorado pronghorn can be a great experience!</p>
